export let weekDay: number = 2;

switch (weekDay) {
  case 1:
    console.log("Es lunes");
    break;
  case 2:
    console.log("Es martes");
    break;
  case 3:
    console.log("Es miercoles");
    break;
  case 4:
    console.log("Es jueves");
    break;
  default:
    console.log("Dia incorrecto");
}
